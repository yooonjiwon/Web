package com.seoul.dto;

import java.util.ArrayList;
import java.util.Date;

public class TureBoard {
	
	private int boardNo;
	private String title;
	private String writer;
	private String content;
	private Date regDate;
	private int readCount;
	private boolean deleted;
	private String location;
	private String time;
	private String day;
	
	private int groupNo;
	private int step;
	private int depth;
	
	private String passNo;
	//Board와 BoardAttach 테이블의 1:m 관계를 구현하는 변수
	ArrayList<TureBoardAttach> attachments;
	//Board와 BoardComment 테이블의 1:m 관계를 구현하는 변수
	ArrayList<TureBoardComment> comments;
	public int getBoardNo() {
		return boardNo;
	}
	public void setBoardNo(int boardNo) {
		this.boardNo = boardNo;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getWriter() {
		return writer;
	}
	public void setWriter(String writer) {
		this.writer = writer;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public Date getRegDate() {
		return regDate;
	}
	public void setRegDate(Date regDate) {
		this.regDate = regDate;
	}
	public int getReadCount() {
		return readCount;
	}
	public void setReadCount(int readCount) {
		this.readCount = readCount;
	}
	public boolean isDelated() {
		return deleted;
	}
	public void setDelated(boolean deleted) {
		this.deleted = deleted;
	}
	public int getGroupNo() {
		return groupNo;
	}
	public void setGroupNo(int groupNo) {
		this.groupNo = groupNo;
	}
	public int getStep() {
		return step;
	}
	public void setStep(int step) {
		this.step = step;
	}
	public int getDepth() {
		return depth;
	}
	public void setDepth(int depth) {
		this.depth = depth;
	}
	public ArrayList<TureBoardAttach> getAttachments() {
		return attachments;
	}
	public void setAttachments(ArrayList<TureBoardAttach> attachments) {
		this.attachments = attachments;
	}
	public ArrayList<TureBoardComment> getComments() {
		return comments;
	}
	public void setComments(ArrayList<TureBoardComment> comments) {
		this.comments = comments;
	}
	public String getPassNo() {
		return passNo;
	}
	public void setPassNo(String passNo) {
		this.passNo = passNo;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getDay() {
		return day;
	}
	public void setDay(String day) {
		this.day = day;
	}
	
	
}
