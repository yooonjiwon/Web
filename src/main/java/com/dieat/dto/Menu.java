package com.dieat.dto;

import java.util.Date;

/**
 * 식단 등록을 위한 Menu의 DTO 폼
 * no : DB에 매겨지는 Menu의 인덱스 번호
 * memberid : 식단을 등록한 유저 아이디
 * foonName : 음식의 이름
 * gram : 음식의 양
 * regDate : 식단 등록 일자
 * meal : 아침식단(1)인지 점심(2)인지 저녁(3)인지 간식(4)인지
 * foodNo : 음식의 인덱스 번호
 * @author HSL
 *
 */
public class Menu {

	private int no;
	private String memberid;
	private String foodName;
	private int gram;
	private Date regDate;
	private int meal;
	private int foonNo;
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public String getMemberid() {
		return memberid;
	}
	public void setMemberid(String memberid) {
		this.memberid = memberid;
	}
	public String getFoodName() {
		return foodName;
	}
	public void setFoodName(String foodName) {
		this.foodName = foodName;
	}
	public int getGram() {
		return gram;
	}
	public void setGram(int gram) {
		this.gram = gram;
	}
	public Date getRegDate() {
		return regDate;
	}
	public void setRegDate(Date regDate) {
		this.regDate = regDate;
	}
	public int getMeal() {
		return meal;
	}
	public void setMeal(int meal) {
		this.meal = meal;
	}
	public int getFoonNo() {
		return foonNo;
	}
	public void setFoonNo(int foonNo) {
		this.foonNo = foonNo;
	}
	
	
	
}
